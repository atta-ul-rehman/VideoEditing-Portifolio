export function getPageTitle(pathname) {
	if (pathname === '/pricing') return 'Pricing'
	if (pathname === '/portfolio') return 'Portfolio'
	if (pathname === '/case-studies') return 'Case Studies'
	if (pathname === '/faq') return 'FAQ'
	if (pathname === '/blog') return 'Blog'

	if (pathname.startsWith('/blog/')) {
		const slug = pathname.replace('/blog/', '')
		const article = articlePages[slug]
		return article ? article.title : 'Blog'
	}

	if (pathname.startsWith('/services/')) {
		const slug = pathname.replace('/services/', '')
		const page = servicePages[slug]
		return page ? page.title : 'Services'
	}

	if (pathname.startsWith('/locations/')) {
		const parts = pathname.split('/').filter(Boolean)
		const cityName = getCityName(parts[1])
		const serviceSlug = parts[2]
		const page = servicePages[serviceSlug]
		if (cityName && page) return `${page.title} ${cityName}`
		return 'Location Services'
	}

	return null
}

